function normalize(s: string): string {
  return s
    .normalize("NFD")
    .replace(/\p{M}/gu, "") // strip combining marks (any Unicode mark)
    .toLowerCase()
    .replace(/['’`]/g, "") // apostrophes (straight + curly)
    .replace(/&/g, " and ")
    .replace(/[-_.]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

