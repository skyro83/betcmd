function fifaToIso2(code: string): string | null {
  const c = (code ?? "").toUpperCase();
  if (!c) return null;
  return FIFA_TO_ISO2[c] ?? c.toLowerCase();
}

export function flagUrl(code: string, override?: string): string | undefined {
  if (override) return override;
  const iso = fifaToIso2(code);
  return iso ? `https://media.api-sports.io/flags/${iso}.svg` : undefined;
}

